package io.github.vennarshulytz.validation.annotation.constraints;

import io.github.vennarshulytz.validation.annotation.ValidateWith;
import io.github.vennarshulytz.validation.constant.MessageConstants;
import io.github.vennarshulytz.validation.validator.builtin.AssertTrueValidator;

import java.lang.annotation.*;

/**
 * 必须为true校验注解
 *
 * @author vennarshulytz
 * @since 1.0.0
 */
@Target(ElementType.PARAMETER)
@Retention(RetentionPolicy.RUNTIME)
@Documented
@ValidateWith(validator = AssertTrueValidator.class)
public @interface AssertTrueCheck {

    String message() default MessageConstants.AssertTrue;
}
